<script>
import axios from "axios";

export default {
  data() {
    return {
      product: null,
      isLoading: true,
      error: null,
      cartItems: [],
      showCart: false
    };
  },
  created() {
    this.fetchProductDetail();
  },
  computed: {
    cartItemCount() {
      return this.cartItems.reduce((total, item) => total + item.quantity, 0);
    },
    totalprix() {
      return this.cartItems.reduce((total, item) => total + item.prix * item.quantity, 0).toFixed(2);
    }
  },
  methods: {
    fetchProductDetail() {
      const productId = this.$route.params.id;
      axios
        .get(`http://localhost:3000/product/${productId}`, {
          headers: {
            role: "client",
          },
        })
        .then((response) => {
          this.product = response.data;
          this.isLoading = false;
        })
        .catch((error) => {
          console.error("Erreur lors de la rÃ©cupÃ©ration du produit:", error);
          this.error = "Erreur lors du chargement des dÃ©tails du produit";
          this.isLoading = false;
        });
    },
    addToCart(product) {
      const existingItem = this.cartItems.find(item => item.id === product.id);
      
      if (existingItem) {
        if (existingItem.quantity < 5) {
          existingItem.quantity += 1;
        } else {
          alert('La quantitÃ© maximale pour cet article est de 5');
        }
      } else {
        this.cartItems.push({ ...product, quantity: 1 });
      }
    },
    removeFromCart(productId) {
      this.cartItems = this.cartItems.filter(item => item.id !== productId);
    },
    goToCart() {
      this.showCart = true;
    },
    closeCart() {
      this.showCart = false;
    }
  }
};
</script>


